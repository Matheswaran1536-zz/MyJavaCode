package basicontrolflow;

public class doWhile {

	public static void main(String[] args) {
		
		int a =0;
		
		
		// DO WHILE
		do{
		
			System.out.println("INSIDE DO BLOCK");
			System.out.println("VALUE OF A "+a);
			a++;
			
		}while(a<2);
		
		
		
				
		
		// WHILE STATEMENT
		
		while(a<0){
			System.out.println("INSIDE WHILE BLOCK");
			System.out.println("VALUE OF A"+a);
			a++;
		}
		
		
			
			
			
			
		}
	}



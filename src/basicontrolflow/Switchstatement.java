package basicontrolflow;

public class Switchstatement {

	public static void main(String[] args) {
		
		
		int a =100;
		
		switch (a) {
		
		case 10 :
		{
			System.out.println("CASE 10 Block");
			break;
		}
		case 20 :
		{
			System.out.println("CASE 20 Block");
			break;

		}
		case 30 :
		{
			System.out.println("CASE 30 Block");
			break;

				
		}
		default:
		{
			System.out.println("Default Block");
			break;

		}

	}

}
}
